package asm

import (
	"fmt"
	"os"
	"path/filepath"
	"strings"

	"github.com/carlmjohnson/versioninfo"
	"github.com/klauspost/asmfmt"
	"github.com/mhr3/gocc/internal/config"
)

// Generate generates the Go PLAN9 assembly
func Generate(arch *config.Arch, srcPath string, clangVersion string, functions []Function) ([]byte, error) {
	var builder strings.Builder

	loadInstructions := arch.LoadInstr
	if loadInstructions == nil {
		loadInstructions = arch.MovInstr
	}

	collectFlags := func() string {
		var flags []string
		for _, f := range arch.ClangFlags {
			if strings.HasPrefix(f, "-masm") {
				continue
			}
			if strings.HasPrefix(f, "-m") {
				flags = append(flags, f)
			}
		}
		res := strings.Join(flags, " ")
		if res == "" {
			return "[none]"
		}
		return res
	}

	builder.WriteString(arch.BuildTags)
	builder.WriteRune('\n')

	fmt.Fprintf(&builder, "// Code generated by gocc %s -- DO NOT EDIT.\n//\n", versioninfo.Short())
	fmt.Fprintf(&builder, "// Source file         : %s\n", filepath.Base(srcPath))
	fmt.Fprintf(&builder, "// Clang version       : %s\n", clangVersion)
	fmt.Fprintf(&builder, "// Target architecture : %s\n", arch.Name)
	fmt.Fprintf(&builder, "// Compiler options    : %s\n", collectFlags())

	builder.WriteString("\n#include \"textflag.h\"\n\n")
	for _, function := range functions {
		for i, c := range function.Consts {
			if i != 0 {
				builder.WriteString("\n")
			}
			builder.WriteString(c.Compile(arch))
		}

		{
			genericParams := 0
			floatParams := 0
			for _, param := range function.Params {
				if param.IsFloatingPoint() {
					floatParams++
				} else {
					genericParams++
				}
			}
			if genericParams > len(arch.Registers) {
				return nil, fmt.Errorf("support for more than %d generic parameters is not implemented for %s", len(arch.Registers), arch.Name)
			}
			if floatParams > len(arch.FloatRegisters) {
				return nil, fmt.Errorf("support for more than %d floating point parameters is not implemented for %s", len(arch.FloatRegisters), arch.Name)
			}
		}

		name := function.Name
		if function.GoFunc.Expr != nil {
			name = function.GoFunc.Name
		}
		paramsSize, offsets := function.ParamsSize(arch)
		retSize := 0
		if function.Ret != nil {
			retSize = function.Ret.Size()
		}
		textAttrs := "NOSPLIT"
		if function.LocalsSize > 0 {
			textAttrs = "0"
		}
		builder.WriteString(fmt.Sprintf("\nTEXT Â·%s(SB),%s,$%d-%d\n", name, textAttrs, function.LocalsSize, paramsSize+retSize))
		genRegIdx, floatRegIdx := 0, 0
		for i, param := range function.Params {
			pSz := int8(param.Size())
			loadInstr, ok := loadInstructions[pSz]
			if !ok {
				return nil, fmt.Errorf("unable to load parameter with size %d", pSz)
			}
			targetRegister := arch.Registers[genRegIdx]
			if param.IsFloatingPoint() {
				loadInstr = arch.MovFPInstr[pSz]
				targetRegister = arch.FloatRegisters[floatRegIdx]
				floatRegIdx++
			} else {
				genRegIdx++
			}
			builder.WriteString(fmt.Sprintf("\t%s %s+%d(FP), %s\n", loadInstr, param.Name, offsets[i], targetRegister))
		}
		for _, line := range function.Lines {
			builder.WriteString(line.Compile(arch))
		}
	}

	return asmfmt.Format(strings.NewReader(builder.String()))
}

// GenerateFile generates the Go PLAN9 assembly file
func GenerateFile(arch *config.Arch, path string, srcPath string, clangVersion string, functions []Function) error {
	functions = ApplyTransforms(arch, functions)
	bytes, err := Generate(arch, srcPath, clangVersion, functions)
	if err != nil {
		return err
	}

	return os.WriteFile(path, bytes, os.ModePerm)
}
