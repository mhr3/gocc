package asm

import (
	"fmt"
	"os"
	"strings"

	"github.com/kelindar/gocc/internal/config"
	"github.com/klauspost/asmfmt"
)

// Generate generates the Go PLAN9 assembly
func Generate(arch *config.Arch, functions []Function) ([]byte, error) {
	var builder strings.Builder

	loadInstructions := arch.LoadInstr
	if loadInstructions == nil {
		loadInstructions = arch.MovInstr
	}

	builder.WriteString(arch.BuildTags)
	fmt.Fprintf(&builder, "\n// Code generated by gocc -- DO NOT EDIT.\n")
	builder.WriteString("\n#include \"textflag.h\"\n\n")
	for _, function := range functions {
		for i, c := range function.Consts {
			if i != 0 {
				builder.WriteString("\n")
			}
			builder.WriteString(c.Compile(arch))
		}

		if len(function.Params) > len(arch.Registers) {
			return nil, fmt.Errorf("support for more than %d parameters is not implemented for %s", len(arch.Registers), arch.Name)
		}

		name := function.Name
		if function.GoFunc.Expr != nil {
			name = function.GoFunc.Name
		}
		paramsSize, offsets := function.ParamsSize(arch)
		retSize := 0
		if function.Ret != nil {
			retSize = function.Ret.Size()
		}
		textAttrs := "NOSPLIT"
		if function.LocalsSize > 0 {
			textAttrs = "0"
		}
		builder.WriteString(fmt.Sprintf("\nTEXT Â·%s(SB),%s,$%d-%d\n", name, textAttrs, function.LocalsSize, paramsSize+retSize))
		for i, param := range function.Params {
			loadInstr, ok := loadInstructions[int8(param.Size())]
			if !ok {
				return nil, fmt.Errorf("unable to load parameter with size %d", param.Size())
			}
			builder.WriteString(fmt.Sprintf("\t%s %s+%d(FP), %s\n", loadInstr, param.Name, offsets[i], arch.Registers[i]))
		}
		for _, line := range function.Lines {
			builder.WriteString(line.Compile(arch))
		}
	}

	return asmfmt.Format(strings.NewReader(builder.String()))
}

// GenerateFile generates the Go PLAN9 assembly file
func GenerateFile(arch *config.Arch, path string, functions []Function) error {
	functions = ApplyTransforms(arch, functions)
	bytes, err := Generate(arch, functions)
	if err != nil {
		return err
	}

	return os.WriteFile(path, bytes, os.ModePerm)
}
